public class Scoreboard {

    public int setsWonPlayerA, setsWonPlayerB, pointsWonPlayerA, pointsWonPlayerB;

    public Scoreboard(int setsWonPlayerA, int setsWonPlayerB, int pointsWonPlayerA, int pointsWonPlayerB) {
        this.setsWonPlayerA = setsWonPlayerA;
        this.setsWonPlayerB = setsWonPlayerB;
        this.pointsWonPlayerA = pointsWonPlayerA;
        this.pointsWonPlayerB = pointsWonPlayerB;
    }

}
